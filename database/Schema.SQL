CREATE DATABASE application;
USE application;

CREATE TABLE application.tbl_streams
(
    id                int4         NOT NULL GENERATED BY DEFAULT AS IDENTITY( INCREMENT BY 1 MINVALUE 1 MAXVALUE 2147483647 START 1 CACHE 1 NO CYCLE),
    class_stream_name varchar(255) NULL,
    CONSTRAINT tbl_streams_pkey PRIMARY KEY (id)
);

CREATE TABLE application.tbl_students
(
    student_id        int4         NOT NULL GENERATED BY DEFAULT AS IDENTITY( INCREMENT BY 1 MINVALUE 1 MAXVALUE 2147483647 START 1 CACHE 1 NO CYCLE),
    class_stream      varchar(255) NULL,
    student_id_number varchar(255) NULL,
    student_name      varchar(255) NULL,
    CONSTRAINT tbl_students_pkey PRIMARY KEY (student_id)
);